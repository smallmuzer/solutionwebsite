import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are the virtual assistant for Systems Solutions Pvt Ltd, a leading IT consulting and software development company based in Malé, Maldives. 

Your role:
- Answer questions about the company's services: Software Development, Web Development, Mobile App Development, ERP Systems, IT Consulting, HR & Payroll, SEO Services, Digital Marketing, Graphic Design
- Provide information about the company: located at Alia Building 7th Floor, Gandhakoalhi Magu, Malé, Maldives. Email: info@solutions.com.mv, Phone: +960 301-1355
- Business hours: Sunday-Thursday 9AM-6PM, Saturday 9AM-1PM
- The company has completed 300+ projects for 50+ clients with 100% satisfaction
- Key clients include OBLU Resorts, OZEN Life, Maldives Stock Exchange, Villa Group, RCSC Bhutan, and many more
- Associated companies: Brilliant Systems Solutions Private Limited, and our Bhutan Company

Be professional, helpful, and concise. If a question is outside your scope, suggest the user contact the company directly. Encourage leads to fill out the contact form or call.`,
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
